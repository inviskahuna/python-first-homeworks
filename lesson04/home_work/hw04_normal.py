def lower_case_near(in_text: str, use_re: bool):
    if use_re:
        return re.findall("[a-z]+", in_text)
    else:
        word = ''
        ret_list = []
        not_re = [x for x in in_text if x in ascii_lowercase]
        for i in in_text:
            if i in not_re:
                word += i
            else:
                ret_list.append(word)
                word = ""
        return [x for x in ret_list if x != ""]


